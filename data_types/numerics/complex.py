# Complex numbers
c1 = 2 + 3j     # Complex number with real part 2 and imaginary part 3
c2 = 1 - 4j     # Complex number with real part 1 and imaginary part -4

# Operations with complex numbers
sum_complex = c1 + c2    # Addition ((2 + 3j) + (1 - 4j) = 3 - 1j)
sub_complex = c1 - c2    # Subtraction ((2 + 3j) - (1 - 4j) = 1 + 7j)
mul_complex = c1 * c2    # Multiplication ((2 + 3j) * (1 - 4j) = 14 - 5j)
div_complex = c1 / c2    # Division ((2 + 3j) / (1 - 4j) = -0.411764705882353 + 0.6470588235294118j)

print(f"Sum (complex): {sum_complex}, Subtraction (complex): {sub_complex}")
print(f"Multiplication (complex): {mul_complex}, Division (complex): {div_complex}")