# Floating-point numbers
a = 3.14     # Positive float
b = -2.5     # Negative float
c = 0.0      # Zero as a float

# Operations with floats
sum_float = a + b    # Addition (3.14 + -2.5 = 0.64)
sub_float = a - b    # Subtraction (3.14 - -2.5 = 5.64)
mul_float = a * b    # Multiplication (3.14 * -2.5 = -7.85)
div_float = a / b    # Division (3.14 / -2.5 = -1.256)

print(f"Sum (float): {sum_float}, Subtraction (float): {sub_float}")
print(f"Multiplication (float): {mul_float}, Division (float): {div_float}")